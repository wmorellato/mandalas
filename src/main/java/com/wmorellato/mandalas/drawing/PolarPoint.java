package com.wmorellato.mandalas.drawing;

/**
 * Polar representation of a point in space.
 */
public class PolarPoint {
    public double r;
    public double theta;

    public PolarPoint(double r, double theta) {
        this.r = r;
        this.theta = theta;
    }
}